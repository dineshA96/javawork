package org.ja;
//we can give read or write access by using getter or setter method
public class Encapsulation {
	private String name;
	private int salary=50000;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSalary() {
		return salary;
	}




}
