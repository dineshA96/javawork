package org.ja;

public class Pojo {
	private String Name;
	private String Id;
	private int Salary;
	//public getters and setters method 
	public String getName() {
		return Name;
	}
	public void setName(String Name) {
		this.Name =Name;
	}
	public String getId() {
		return Id;
	}
	public void setId(String Id) {
		this.Id =Id;
	}
	public int getSalary() {
		return Salary;
	}
	public void setSalary(int Salary) {
		this.Salary =Salary;
	}

	

	

}
