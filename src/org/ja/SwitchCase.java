package org.ja;

public class SwitchCase {
	public static void main(String[] args) {
		int abc=5;
		switch(abc){
		case 1:
			System.out.println("one");
			break;
		case 2:
			System.out.println("five");
			break;
		case 3:
			System.out.println("six");
			break;
		case 4:
			System.out.println("seven");
			break;
		case 5:
			System.out.println("eight");
			break;
		default:
			System.out.println("invlaid");
			break;
		}
	}

}
